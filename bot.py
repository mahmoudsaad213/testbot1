def parse_proxy(proxy_string):
    """تحويل البروكسي من صيغة ip:port:user:pass إلى dict"""
    try:
        parts = proxy_string.strip().split(':')
        if len(parts) != 4:
            return None
        
        ip, port, username, password = parts
        
        # صيغة SOCKS5
        proxy_dict = {
            'http': f'socks5://{username}:{password}@{ip}:{port}',
            'https': f'socks5://{username}:{password}@{ip}:{port}'
        }
        
        return {
            'proxies': proxy_dict,
            'string': proxy_string,
            'ip': ip,
            'port': port,
            'username': username
        }
    except Exception as e:
        logger.error(f"❌ Parse proxy error: {e}")
        return None

